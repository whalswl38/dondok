

DROP TABLE KEYSEARCH;

CREATE TABLE KEYSEARCH(
	M_ID VARCHAR(1000) NOT NULL,
	M_SEARCH VARCHAR(4000) NOT NULL,
	CONSTRAINTS M_ID_FK FOREIGN KEY(M_ID) REFERENCES MYMEMBER(M_ID) 
);

