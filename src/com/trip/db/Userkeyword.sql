SELECT ROWNUM, A.*
FROM( SELECT M_SEARCH, COUNT(*) AS 검색개수 FROM KEYSEARCH WHERE M_ID='KIM' GROUP BY M_SEARCH ORDER BY 검색개수 DESC ) A
WHERE ROWNUM<=3;








