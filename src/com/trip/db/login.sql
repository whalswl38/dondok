DROP SEQUENCE MEMBERSEQ;
DROP TABLE MYMEMBER;

CREATE SEQUENCE MEMBERSEQ;

CREATE TABLE MYMEMBER(

M_NO NUMBER,
M_ID VARCHAR2(250) PRIMARY KEY,
M_NICK VARCHAR2(250) NOT NULL,
M_PASS VARCHAR2(250) NOT NULL,
M_NAME VARCHAR2(250) NOT NULL,
M_EMAIL VARCHAR2(300) NOT NULL,
M_PHONE VARCHAR2(300) NOT NULL,
M_FLAG VARCHAR2(1) NOT NULL,
M_GRADE VARCHAR2(100) NOT NULL,

CONSTRAINT MEMBER_ID UNIQUE (M_NICK),
CONSTRAINT MEMBER_PHONE UNIQUE (M_PHONE),
CONSTRAINT MEMBER_EMAIL UNIQUE (M_EMAIL)
);

